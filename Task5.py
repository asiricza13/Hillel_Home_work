'''
ДЗ 5. Додаткове Д3

Написати програму, яка переміщає всі нулі в кінці списку.
Ваша задача — змінити список так, щоб всі нулі опинились в кінці списку.
Порядок всіх інших чисел повинен зберегтися!
'''

lst = [0, 1, 0, 3, 12]
cnt = lst.count(0)

while lst.count(0) > 0:
    lst.remove(0)

lst.extend([0]*cnt)
print(lst)











